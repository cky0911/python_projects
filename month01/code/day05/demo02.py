"""
    深拷贝和浅拷贝
        浅拷贝：复制过程中,只复制一层变量,不会复制深层变量绑定的对象的复制过程。
        深拷贝：复制整个依赖的变量。
"""
import copy

list01 = [800, [1000, 500]]
# 浅拷贝
# list02 = list01[:]
list02 = list01.copy()  # 与上一行的切片等价
list01[1][0] = 900
print(list02[1][0])  # ?900

list01 = [800, [1000, 500]]
# 深拷贝
list02 = copy.deepcopy(list01)
list01[1][0] = 900
print(list02[1][0])  # 1000
